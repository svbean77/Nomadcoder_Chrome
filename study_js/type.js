/*
변수 선언

const: 변경할 수 없는 변수 선언
let: 변경할 수 있는 변수 선언
*/

/*
변수 타입
number: 숫자
string: 문자열
boolean: 참/거짓
array: 배열
object: 객체
*/

//정수
const integer = 1;
//실수
const float = 1.5;
//문자열
const string = "hello";
//참, 거짓
const boolean = false;
//비어있는 값
const none = null;
//정의되지 않은 값
let undefinedType;

//1 1.5 "hello" false null undefined
console.log(integer, float, string, boolean, none, undefinedType);
